#include "pilha.h"

struct pilha * create (){
	
	struct pilha *P = NULL;

	P = (struct pilha*) malloc(sizeof(struct pilha));

	if(P == NULL){
		return NULL;
	}
	
	P->topo = 0;
	return P;
}
////////////////////////////////////////////////////////////////////////////////////////	
int makenull (struct pilha * p){

	if(p == NULL){
		return 0;
	}
	if (vazia(p) == 1){
		return 0;
	}
	while (p != NULL){
		pop(p);
	}
	return 1;
}
///////////////////////////////////////////////////////////////////////////////////////
int top(struct pilha * p){
    if (p == NULL ){
        return 0;
    }
    return p->vetorItem[p->topo-1];
    }
////////////////////////////////////////////////////////////////////////////////////////
int pop(struct pilha *p){
	if(p == NULL || vazia(p) == 1){
		return 0;
	}
	p->topo--; 
	return 1;
}
////////////////////////////////////////////////////////////////////////////////////////
int push (struct pilha * p, int val){
	if (p == NULL){
		return 0;
	}
	
	p->vetorItem[p->topo] = val;
	p->topo++;
	return 1;
}
/////////////////////////////////////////////////////////////////////////////////////////
int vazia (struct pilha * p){
	if (p == NULL || p->topo != 0){
		return 0;
	}
	   return 1;
}

////////////////////////////////////////////////////////////////////////////////////////
void destroy (struct pilha * p){	
	
	free(p);
}
